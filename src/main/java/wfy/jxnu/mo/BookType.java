package wfy.jxnu.mo;

public class BookType {
    private String bookTypeName;
    private int bookTypeSaledNum;  //销售数量

    public String getBookTypeName() {
        return bookTypeName;
    }

    public void setBookTypeName(String bookTypeName) {
        this.bookTypeName = bookTypeName;
    }

    public int getBookTypeSaledNum() {
        return bookTypeSaledNum;
    }

    public void setBookTypeSaledNum(int bookTypeSaledNum) {
        this.bookTypeSaledNum = bookTypeSaledNum;
    }
}
